#append method
fruits=[]
fruits.append('mango')
fruits.append('grapes')
print(fruits)

#insert method
fruits1=['mango','grapes']
fruits1.insert(1,'guavava')
print(fruits1)

#joining two list

fruits2=['cheeka','mandi']
fruits3=fruits1+fruits2
print(fruits3)

#extend method

fruits1.extend(fruits2)
print(fruits1)